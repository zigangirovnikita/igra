const rub = (n: number) =>
  n.toLocaleString('ru-RU', { style: 'currency', currency: 'RUB', maximumFractionDigits: 0 });

const days = (n: number) => (n === 1 ? '1 день' : n <= 4 ? `${n} дня` : `${n} дней`);

